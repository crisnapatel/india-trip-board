function renderWikiPage() {
  const params = new URLSearchParams(location.search);
  const id = params.get("id");
  const place = places.find((item) => item.id === id);

  if (!place) {
    renderMissingPage();
    return;
  }

  const override = wikiOverrides[place.id] || {};
  const category = categoryWiki[place.category] || categoryWiki.Experience;
  const intro = buildIntro(place, override, category);

  document.title = `${place.name} · Place wiki`;
  setText("#wikiMeta", `${place.city} · ${place.category}`);
  setText("#wikiTitle", place.name);
  setText("#wikiIcon", getCategoryIcon(place.category));
  setText("#wikiSeeTitle", override.seeTitle || category.seeTitle || "Things to see / try");

  document.querySelector("#wikiMap").href = `https://www.google.com/maps/search/?api=1&query=${encodeURIComponent(`${place.name} ${place.city}`)}`;

  renderParagraphs("#wikiIntro", intro);
  renderList("#wikiNotice", override.notice || category.notice);
  renderList("#wikiSee", override.see || category.see);
  renderList("#wikiUseful", override.useful || category.useful);
}

function renderMissingPage() {
  document.title = "Place not found";
  document.querySelector("#wikiMap").hidden = true;
  setText("#wikiMeta", "Missing page");
  setText("#wikiTitle", "Place not found");
  setText("#wikiIcon", "📍");
  setText("#wikiSeeTitle", "What to do");
  renderParagraphs("#wikiIntro", ["This wiki page does not match a place in the current trip list."]);
  renderList("#wikiNotice", ["The link may be old, mistyped, or from a removed place."]);
  renderList("#wikiSee", ["Open a place card's ⓘ icon again."]);
  renderList("#wikiUseful", ["Use the Back link to return to the current list."]);
}

function buildIntro(place, override, category) {
  if (Array.isArray(override.intro)) return override.intro;
  if (override.intro) return [override.intro];

  const base = placeDescriptions[place.id] || place.notes || "Useful stop for this trip.";
  return [base, category.introAdd];
}

function renderParagraphs(selector, paragraphs) {
  const container = document.querySelector(selector);
  container.replaceChildren();
  for (const paragraph of paragraphs) {
    const p = document.createElement("p");
    p.textContent = paragraph;
    container.append(p);
  }
}

function renderList(selector, items) {
  const list = document.querySelector(selector);
  list.replaceChildren();
  for (const item of items) {
    const li = document.createElement("li");
    li.textContent = item;
    list.append(li);
  }
}

function setText(selector, text) {
  document.querySelector(selector).textContent = text;
}
